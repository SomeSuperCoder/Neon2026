func TestCodeGenerator_NoEntryFunction(t *testing.T) {
	// Create a program without an entry function
	program := &Program{
		Declarations: []Node{
			&FunctionDecl{
				Name:       "helper",
				IsExport:   false,
				Parameters: []*Parameter{},
				ReturnType: &TypeAnnotation{Name: "void"},
				Body: &BlockStmt{
					Statements: []Statement{
						&ReturnStmt{},
					},
				},
			},
		},
	}

	// Generate bytecode
	cg := NewCodeGenerator()
	_, err := cg.Generate(program)
	if err == nil {
		t.Fatal("Expected error for missing entry function, got nil")
	}

	expectedMsg := "no exported 'entry' function found"
	if err.Error() != expectedMsg {
		t.Errorf("Expected error message '%s', got '%s'", expectedMsg, err.Error())
	}
}
