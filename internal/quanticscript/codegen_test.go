func TestCodeGenerator_SimpleFunction(t *testing.T) {
	// Create a simple program with an entry function
	program := &Program{
		Declarations: []Node{
			&FunctionDecl{
				Name:       "entry",
				IsExport:   true,
				Parameters: []*Parameter{},
				ReturnType: &TypeAnnotation{Name: "void"},
				Body: &BlockStmt{
					Statements: []Statement{
						&ReturnStmt{},
					},
				},
			},
		},
	}

	// Generate bytecode
	cg := NewCodeGenerator()
	bytecode, err := cg.Generate(program)
	if err != nil {
		t.Fatalf("Failed to generate bytecode: %v", err)
	}

	// Verify bytecode is not empty
	if len(bytecode) == 0 {
		t.Fatal("Generated bytecode is empty")
	}

	// Verify it starts with CALL (entry wrapper)
	if bytecode[0] != byte(OpCall) {
		t.Errorf("Expected first opcode to be CALL, got %v", Opcode(bytecode[0]))
	}
}

func TestCodeGenerator_ArithmeticExpression(t *testing.T) {
	// Create a program with arithmetic: return 5 + 3;
	program := &Program{
		Declarations: []Node{
			&FunctionDecl{
				Name:       "entry",
				IsExport:   true,
				Parameters: []*Parameter{},
				ReturnType: &TypeAnnotation{Name: "i64"},
				Body: &BlockStmt{
					Statements: []Statement{
						&ReturnStmt{
							Value: &BinaryExpr{
								Left:     &IntLiteral{Value: "5"},
								Operator: TOKEN_PLUS,
								Right:    &IntLiteral{Value: "3"},
							},
						},
					},
				},
			},
		},
	}

	// Generate bytecode
	cg := NewCodeGenerator()
	bytecode, err := cg.Generate(program)
	if err != nil {
		t.Fatalf("Failed to generate bytecode: %v", err)
	}

	// Verify bytecode contains PUSH, ADD, and RET instructions
	hasAdd := false
	hasRet := false
	for _, b := range bytecode {
		if Opcode(b) == OpAdd {
			hasAdd = true
		}
		if Opcode(b) == OpRet {
			hasRet = true
		}
	}

	if !hasAdd {
		t.Error("Expected ADD instruction in bytecode")
	}
	if !hasRet {
		t.Error("Expected RET instruction in bytecode")
	}
}

